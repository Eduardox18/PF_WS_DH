package servicios.pojos;

public class Fotografia {
    private Integer idFotografia;
    private byte[] foto;
    private Integer idReporte;
    private String fechaHora;

    public Fotografia() {
    }

    public Fotografia(Integer idFotografia, byte[] foto, Integer idReporte, String fechaHora) {
        this.idFotografia = idFotografia;
        this.foto = foto;
        this.idReporte = idReporte;
        this.fechaHora = fechaHora;
    }

    public Integer getIdFotografia() {
        return idFotografia;
    }

    public void setIdFotografia(Integer idFotografia) {
        this.idFotografia = idFotografia;
    }

    public byte[] getFoto() {
        return foto;
    }

    public void setFoto(byte[] foto) {
        this.foto = foto;
    }

    public Integer getIdReporte() {
        return idReporte;
    }

    public void setIdReporte(Integer idReporte) {
        this.idReporte = idReporte;
    }

    public String getFechaHora() {
        return fechaHora;
    }

    public void setFechaHora(String fechaHora) {
        this.fechaHora = fechaHora;
    }
    
    
}
